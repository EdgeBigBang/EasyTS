class Score():

    def __init__(self, args, current_dataset, dataset):
        self.args = args
        self.device = self._acquire_device()
        self.model = self._build_model().to(self.device)
        self.dataset = dataset
        self.current_dataset = current_dataset

    def _build_model(self):
        ## If adding a custom model, please import it in the file
        model = eval(self.args.model).Model(self.args).float()

        if self.args.use_multi_gpu and self.args.use_gpu:
            model = nn.DataParallel(model, device_ids=self.args.device_ids)

        return model

    def _get_data(self, flag):
        ## If adding a custom dataset, please add an entry here and import it in the file
        data_dict = {
            'Transportation': Dataset_Transportation,
            'Electricity': Dataset_Electricity,
            'Energy': Dataset_Energy,
            'Disease': Dataset_Disease,
            'Weather': Dataset_Weather
        }

        Data = data_dict[self.current_dataset]
        # Download dataset
        data_path, dataset_path = Data.download(self.dataset)

        timeenc = 0 if self.args.embed != 'timeF' else 1
        # Test
        if flag == 'test':
            shuffle_flag = False
            drop_last = True
            batch_size = self.args.batch_size
            freq = self.args.freq
        # Train
        else:
            shuffle_flag = True
            drop_last = True
            batch_size = self.args.batch_size
            freq = self.args.freq

        data_set = Data(
            data_path=data_path,
            dataset_path = dataset_path,
            flag=flag,
            size=[self.args.seq_len, self.args.label_len, self.args.pred_len],
            features=self.args.features,
            target=self.args.target,
            timeenc=timeenc,
            freq=freq
        )
        print(flag, len(data_set))
        data_loader = DataLoader(
            data_set,
            batch_size=batch_size,
            shuffle=shuffle_flag,
            num_workers=self.args.num_workers,
            drop_last=drop_last)

        return data_set, data_loader

    def _acquire_device(self):
        if self.args.use_gpu:
            os.environ["CUDA_VISIBLE_DEVICES"] = str(
                self.args.gpu) if not self.args.use_multi_gpu else self.args.devices
            device = torch.device('cuda:{}'.format(self.args.gpu))
            print('Use GPU: cuda:{}'.format(self.args.gpu))
        else:
            device = torch.device('cpu')
            print('Use CPU')
        return device

    def _select_optimizer(self):
        model_optim = optim.Adam(self.model.parameters(), lr=self.args.learning_rate)
        return model_optim

    def _select_criterion(self):
        training_metric = self.args.training_metric


        metrics = {
            'mse': nn.MSELoss(),
            'mae': nn.L1Loss(),
                   }

        criterion = metrics[training_metric]
        return criterion

    def metrics_to_dict(self, metrics_str):
        metrics_dict = {}

        key_value_pairs = metrics_str.split(', ')
        for pair in key_value_pairs:
            key, value = pair.split(':')
            metrics_dict[key.strip()] = float(value)
        return metrics_dict


    def vali(self, vali_data, vali_loader, criterion):
        total_loss = []
        self.model.eval()
        with torch.no_grad():
            for i, (batch_x, batch_y, batch_x_mark, batch_y_mark) in enumerate(vali_loader):
                batch_x = batch_x.float().to(self.device)
                batch_y = batch_y.float()

                batch_x_mark = batch_x_mark.float().to(self.device)
                batch_y_mark = batch_y_mark.float().to(self.device)

                # decoder input
                dec_inp = torch.zeros_like(batch_y[:, -self.args.pred_len:, :]).float()
                dec_inp = torch.cat([batch_y[:, :self.args.label_len, :], dec_inp], dim=1).float().to(self.device)
                # encoder - decoder
                if self.args.use_amp:
                    with torch.cuda.amp.autocast():
                        if 'Linear' in self.args.model:
                            outputs = self.model(batch_x)
                        else:
                            if self.args.output_attention:
                                outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)[0]
                            else:
                                outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)
                else:
                    if 'Linear' in self.args.model:
                        outputs = self.model(batch_x)
                    else:
                        if self.args.output_attention:
                            outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)[0]
                        else:
                            outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)
                f_dim = -1 if self.args.features == 'MS' else 0
                outputs = outputs[:, -self.args.pred_len:, f_dim:]
                batch_y = batch_y[:, -self.args.pred_len:, f_dim:].to(self.device)

                pred = outputs.detach().cpu()
                true = batch_y.detach().cpu()

                loss = criterion(pred, true)

                total_loss.append(loss)
        total_loss = np.average(total_loss)
        self.model.train()
        return total_loss

